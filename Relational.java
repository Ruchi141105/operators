public class Relational {
    public static void main(String[] args) {
        int num1=5, num2=10;
        System.out.println("num1="+num1+"  num2="+num2);
        System.out.println(num1>num2);
        System.out.println(num1<num2);
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);
        System.out.println(num1==num2);
        System.out.println(num1!=num2);       
    }
}
